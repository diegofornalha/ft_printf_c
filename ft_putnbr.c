#include "ft_printf.h"

void	ft_putnbr(int n, int *p){
    if (n < 0)
    {
        ft_putchar('-');
        n = -n;
    }
    if (n >= 10)
        ft_putnbr(n / 10, p);
    ft_putchar(n % 10 + '0');
    (*p)++;
}
